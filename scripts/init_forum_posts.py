async def get_or_create_author(session: AsyncSession) -> User:
    result = await session.execute(select(User).where(User.phone == AUTHOR_PHONE))
    user = result.scalar_one_or_none()
    if user:
        return user

    user = User(
        nickname=AUTHOR_NICKNAME,
        phone=AUTHOR_PHONE,
        password_hash=SecurityService.get_password_hash(AUTHOR_PASSWORD),
    )
    session.add(user)
    await session.commit()
    await session.refresh(user)
    return user


async def init_forum_posts():
    engine = create_async_engine(settings.DATABASE_URL, echo=False)

    async with engine.begin() as conn:
        await conn.run_sync(Base.metadata.create_all)

    async_session = async_sessionmaker(engine, class_=AsyncSession, expire_on_commit=False)

    async with async_session() as session:
        author = await get_or_create_author(session)
        created_count = 0
        updated_count = 0
        skipped_count = 0

        for post_data in FORUM_POSTS:
            result = await session.execute(
                select(Post).where(Post.title == post_data["title"])
            )
            existing_post = result.scalars().first()
            if existing_post:
                if existing_post.tag != post_data["tag"]:
                    existing_post.tag = post_data["tag"]
                    updated_count += 1
                else:
                    skipped_count += 1
                continue

            session.add(
                Post(
                    user_id=author.id,
                    title=post_data["title"],
                    tag=post_data["tag"],
                    content=post_data["content"],
                )
            )
            created_count += 1

        await session.commit()
        print(
            f"Initialized {created_count} forum posts "
            f"({updated_count} updated, {skipped_count} already existed, {len(FORUM_POSTS)} total)"
        )

    await engine.dispose()


if __name__ == "__main__":
    asyncio.run(init_forum_posts())
